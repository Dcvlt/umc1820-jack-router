# Function to create required configuration files
setup_configuration_files() {
    echo -e "\n${CYAN}‚öôÔ∏è Setting up configuration files...${NC}"
    
    # Mosquitto configuration
    local mosquitto_conf="$PROJECT_DIR/mosquitto/config/mosquitto.conf"
    if [[ ! -f "$mosquitto_conf" ]]; then
        cat > "$mosquitto_conf" << 'EOF'
# Mosquitto MQTT Broker Configuration
listener 1883
allow_anonymous true
persistence true
persistence_location /mosquitto/data/
log_dest file /mosquitto/log/mosquitto.log
log_dest stdout
log_type error
log_type warning
log_type notice
log_type information
connection_messages true
log_timestamp true

# WebSocket support
listener 9001
protocol websockets
allow_anonymous true
EOF
        echo -e "${GREEN}‚úÖ Created Mosquitto configuration${NC}"
    else
        echo -e "${CYAN}‚ÑπÔ∏è Mosquitto configuration already exists${NC}"
    fi
    
    # JACK Bridge package.json
    local bridge_package="$PROJECT_DIR/jack-bridge/package.json"
    if [[ ! -f "$bridge_package" ]]; then
        cat > "$bridge_package" << 'EOF'
{
  "name": "jack-bridge-temp",
  "version": "1.0.0",
  "description": "Temporary JACK Bridge Service (Node.js)",
  "main": "app.js",
  "scripts": {
    "start": "node app.js",
    "dev": "nodemon app.js"
  },
  "dependencies": {
    "express": "^4.18.2",
    "ws": "^8.14.0"
  },
  "engines": {
    "node": ">=18.0.0"
  }
}
EOF
        echo -e "${GREEN}‚úÖ Created JACK Bridge package.json${NC}"
    else
        echo -e "${CYAN}‚ÑπÔ∏è JACK Bridge package.json already exists${NC}"
    fi
}

# Function to check required files
check_required_files() {
    echo -e "\n${CYAN}üìã Checking required files...${NC}"
    
    local required_files=(
        "package.json"
        "server.js"
        "docker-compose.yml"
        "Dockerfile"
        "jack-bridge/app.js"
        "jack-bridge/package.json"
        "jack-bridge/Dockerfile"
        "mosquitto/config/mosquitto.conf"
    )
    
    local missing_files=()
    
    for file in "${required_files[@]}"; do
        if [[ ! -f "$PROJECT_DIR/$file" ]]; then
            missing_files+=("$file")
            echo -e "${RED}‚ùå Missing: $file${NC}"
        else
            echo -e "${GREEN}‚úÖ Found: $file${NC}"
        fi
    done
    
    if [[ ${#missing_files[@]} -gt 0 ]]; then
        echo -e "\n${RED}‚ùå Missing required files. Please create these files first:${NC}"
        for file in "${missing_files[@]}"; do
            echo -e "   ${YELLOW}- $file${NC}"
        done
        echo -e "\n${YELLOW}These files should have been provided in the implementation guide.${NC}"
        return 1
    fi
    
    echo -e "${GREEN}‚úÖ All required files are present${NC}"
    return 0
}

# Function to build Docker images
build_images() {
    echo -e "\n${CYAN}üî® Building Docker images...${NC}"
    echo -e "${YELLOW}This may take several minutes on first run...${NC}"
    
    cd "$PROJECT_DIR"
    
    # Build with progress output
    if docker-compose build --progress=plain; then
        echo -e "${GREEN}‚úÖ Docker images built successfully${NC}"
    else
        echo -e "${RED}‚ùå Failed to build Docker images${NC}"
        echo -e "${YELLOW}Check the output above for error details${NC}"
        return 1
    fi
}

# Function to start services
start_services() {
    echo -e "\n${CYAN}üöÄ Starting services...${NC}"
    
    cd "$PROJECT_DIR"
    
    if docker-compose up -d; then
        echo -e "${GREEN}‚úÖ Services started successfully${NC}"
        
        echo -e "\n${YELLOW}‚è≥ Waiting for services to initialize...${NC}"
        
        # Wait for services with progress indication
        local max_wait=60
        local wait_time=0
        local services_ready=false
        
        while [[ $wait_time -lt $max_wait ]]; do
            sleep 2
            wait_time=$((wait_time + 2))
            
            # Check if key services are responding
            local bridge_ok=false
            local router_ok=false
            
            if curl -s -f http://localhost:6666/health >/dev/null 2>&1; then
                bridge_ok=true
            fi
            
            if curl -s -f http://localhost:3001/health >/dev/null 2>&1; then
                router_ok=true
            fi
            
            if [[ "$bridge_ok" == "true" && "$router_ok" == "true" ]]; then
                services_ready=true
                break
            fi
            
            echo -e "${CYAN}   Waiting... (${wait_time}s/${max_wait}s)${NC}"
        done
        
        if [[ "$services_ready" == "true" ]]; then
            echo -e "${GREEN}‚úÖ All services are ready!${NC}"
        else
            echo -e "${YELLOW}‚ö†Ô∏è Some services may still be starting up${NC}"
        fi
        
    else
        echo -e "${RED}‚ùå Failed to start services${NC}"
        echo -e "${YELLOW}Check logs with: docker-compose logs${NC}"
        return 1
    fi
}

# Function to perform health checks
health_check() {
    echo -e "\n${CYAN}üè• Performing health checks...${NC}"
    
    local checks=(
        "http://localhost:6666/health:JACK Bridge"
        "http://localhost:3001/health:JACK Router"
        "http://localhost:1883:MQTT Broker"
    )
    
    local all_healthy=true
    
    for check in "${checks[@]}"; do
        local url="${check%:*}"
        local service="${check#*:}"
        
        if [[ "$service" == "MQTT Broker" ]]; then
            # MQTT check is different (just check if port is open)
            if nc -z localhost 1883 >/dev/null 2>&1; then
                echo -e "${GREEN}‚úÖ $service is healthy${NC}"
            else
                echo -e "${RED}‚ùå $service is not healthy${NC}"
                all_healthy=false
            fi
        else
            if curl -s -f "$url" >/dev/null 2>&1; then
                echo -e "${GREEN}‚úÖ $service is healthy${NC}"
            else
                echo -e "${RED}‚ùå $service is not healthy${NC}"
                all_healthy=false
            fi
        fi
    done
    
    return $([[ "$all_healthy" == "true" ]])
}

