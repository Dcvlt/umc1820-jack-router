# Function to check prerequisites
check_prerequisites() {
    echo -e "${CYAN}üîç Checking prerequisites...${NC}"
    
    local prerequisites_ok=true
    
    # Check Docker
    if ! command -v docker &> /dev/null; then
        echo -e "${RED}‚ùå Docker is not installed${NC}"
        echo -e "${YELLOW}Please install Docker Desktop: https://www.docker.com/products/docker-desktop${NC}"
        prerequisites_ok=false
    else
        echo -e "${GREEN}‚úÖ Docker found: $(docker --version | head -n1)${NC}"
    fi
    
    # Check Docker Compose
    if ! command -v docker-compose &> /dev/null; then
        echo -e "${RED}‚ùå Docker Compose is not installed${NC}"
        echo -e "${YELLOW}Please install Docker Compose${NC}"
        prerequisites_ok=false
    else
        echo -e "${GREEN}‚úÖ Docker Compose found: $(docker-compose --version)${NC}"
    fi
    
    # Check if Docker is running
    if ! docker info &> /dev/null; then
        echo -e "${RED}‚ùå Docker daemon is not running${NC}"
        echo -e "${YELLOW}Please start Docker Desktop${NC}"
        prerequisites_ok=false
    else
        echo -e "${GREEN}‚úÖ Docker daemon is running${NC}"
    fi
    
    # Check available disk space (require at least 2GB free)
    local available_space=$(df "$PROJECT_DIR" | awk 'NR==2 {print $4}')
    local required_space=$((2 * 1024 * 1024)) # 2GB in KB
    
    if [[ $available_space -lt $required_space ]]; then
        echo -e "${RED}‚ùå Insufficient disk space. Required: 2GB, Available: $((available_space / 1024 / 1024))GB${NC}"
        prerequisites_ok=false
    else
        echo -e "${GREEN}‚úÖ Sufficient disk space available${NC}"
    fi
    
    if [[ "$prerequisites_ok" != "true" ]]; then
        echo -e "${RED}‚ùå Prerequisites check failed. Please fix the issues above.${NC}"
        exit 1
    fi
    
    echo -e "${GREEN}‚úÖ All prerequisites satisfied${NC}"
}

# Function to create required directories
create_directories() {
    echo -e "\n${CYAN}üìÅ Creating required directories...${NC}"
    
    local dirs=(
        "jack-bridge"
        "mosquitto/config"
        "logs"
        "ssl"
        "state"
        "scripts"
    )
    
    for dir in "${dirs[@]}"; do
        if [[ ! -d "$PROJECT_DIR/$dir" ]]; then
            mkdir -p "$PROJECT_DIR/$dir"
            echo -e "${GREEN}‚úÖ Created $dir${NC}"
        else
            echo -e "${CYAN}‚ÑπÔ∏è Directory $dir already exists${NC}"
        fi
    done
}

